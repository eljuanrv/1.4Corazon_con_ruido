# convierte la matriz anterior en una matriz numerica
z=[]
for elemento in ruido:
  for cadena in elemento:
    lista_1=[]
    for numero in cadena:
      lista_1.append(int(numero))
    z.append(lista_1)

m=len(z[0])
n=len(z)

#es el ciclo encnargado de limpiar el ruido
for r in range(1,n-1):
  for c in range(1,m-1):
    if(z[r-1][c]==1 and z[r+1][c]==1 and z[r][c-1]==1 and z[r][c+1]==1):
      z[r][c]=1 #central


# se encarga de imprimir el corazon en forma rectangular
